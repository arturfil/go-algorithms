package linkedlists

func addTwoNumbers(l1 *ListNode, l2 *ListNode) *ListNode {
	dummy := &ListNode{Val: 0}
	current := dummy
    carry := 0
    
    for l1 != nil || l2 != nil || carry > 0 {
		sum := carry 

		if l1 != nil { 
			sum += l1.Val
			l1 = l1.Next
		}

		if l2 != nil {
			sum += l2.Val
			l2 = l2.Next
		}

		current.Next = &ListNode{Val: sum % 10}
		current = current.Next
		
		carry = sum / 10
    }

    return dummy.Next
}
